An extinct genus of bird-like dinosaurs that lived around 150 million years ago during the Late Jurassic period.


It represents an important evolutionary link between non-avian theropod dinosaurs and birds, although it is not the direct ancestor of modern birds. 


The first complete fossil was found in Germany in 1861, and its features include a blend of reptilian and avian characteristics. 


Archaeopteryx played a significant role in scientific debates about the origin and evolution of birds

