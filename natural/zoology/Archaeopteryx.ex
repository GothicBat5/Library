Over the years, fourteen body fossil specimens of Archaeopteryx have been found. 
All of the fossils come from the limestone deposits, quarried for centuries, near Solnhofen, Germany. 
These quarries excavate sediments from the Solnhofen Limestone formation and related units.[17][18] 
The initial specimen was the first dinosaur to be discovered with feathers.
Timeline of Archaeopteryx discoveries until 2007
The initial discovery, a single feather, was unearthed in 1860 or 1861 and described in 1861 by Hermann von Meyer.[19] 
It is now in the Natural History Museum of Berlin. Though it was the initial holotype, there were indications that it might not have been from the same animal as the body fossils.[11] 
In 2019 it was reported that laser imaging had revealed the structure of the quill (which had not been visible since some time after the feather was described),
and that the feather was inconsistent with the morphology of all other Archaeopteryx feathers known, leading to the conclusion that it originated from another dinosaur.[12]
This conclusion was challenged in 2020 as being unlikely; the feather was identified on the basis of morphology as most likely having been an upper major primary covert feather.[13]
